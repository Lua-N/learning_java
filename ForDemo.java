package com.mycompany.iniciandojava;

public class ForDemo {
    public static void main(String args[]){
        int count;
        
        for(count = 0; count < 5; count = count + 1) //este laço itera 5 vezes
            System.out.println("Este é o contador: " + count);
        //enquanto a condição for verdadeira, ele vai executar o comando.
        System.out.println("Feito!");
            
     
            
    }
    
}
